function fillSelectWithhmtlTags(selectid)
{
    var slct = document.getElementById(selectid);  
	for (var ic = 0; ic < htmlTagCategory.length; ic++)
	{
		slct.innerHTML+="<optgroup label=\"" + htmlTagCategory[ic] +"\"></optgroup>";		
		//var s="";
		for (var it=0;it<htmlTags[ic].length;it++)
		{
			// Create a <button> element
			//var t = document.createTextNode("CLICK ME");       // Create a text node
			//slct.appendChild(t);                                // Append the text to <button>
			var opt = document.createElement("OPTION"); 
			opt.label=htmlTags[ic][it].tag;
			opt.value=[ic]+","+[it];
			slct.appendChild(opt);
			//document.body.appendChild(slct);                    // Append <button> to <body>
			//s+="<option>&lt;"+htmlTags[index][k].tag+"&gt;</option>";
		}
		
	}
}

